public class ScoreGson {

    /**
     * id : 2
     * score : 26
     * time : 2018-10-24 12:14:10
     * uid : 1
     * coin : 230
     */

    private int id;
    private String score;
    private String time;
    private int uid;
    private String coin;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getCoin() {
        return coin;
    }

    public void setCoin(String coin) {
        this.coin = coin;
    }

}
